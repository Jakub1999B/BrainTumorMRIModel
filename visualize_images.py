def data_loader(class_names, train_path, test_path, class_map):
    images = []
    labels = []

    image_size = 250

    for i in class_names:
      folderPath = os.path.join(train_path, i)
      for j in tqdm(os.listdir(folderPath)):
        image = cv2.imread(os.path.join(folderPath, j), 0)
        image = cv2.resize(image, (image_size, image_size))
        images.append(image)
        labels.append(class_map[i])

    for i in class_names:
      folderPath = os.path.join(test_path, i)
      for j in tqdm(os.listdir(folderPath)):
        image = cv2.imread(os.path.join(folderPath, j), 0)
        image = cv2.resize(image, (image_size, image_size))
        images.append(image)
        labels.append(class_map[i])
    
    images = np.array(images)
    labels = np.array(labels)
    images, labels = sklearn.utils.shuffle(images, labels, random_state=0)
        
    return images, labels

def img_viz(train_path, test_path):
  class_names = ['glioma_tumor','meningioma_tumor','no_tumor','pituitary_tumor']
  class_map = {'glioma_tumor' : 0, 'meningioma_tumor' : 1, 'no_tumor' : 2, 'pituitary_tumor' : 3}
  images, labels = data_loader(class_names, train_path, test_path, class_map)
  plt.rcParams.update({'font.size': 20})
  plt.rcParams['font.family'] = 'serif'
  plt.rcParams['font.serif'] = ['Times New Roman'] + plt.rcParams['font.serif']

  plt.figure(figsize = (20,20), dpi = 200)
  for i in range(16):
      plt.subplot(4, 4, i+1)
      x = np.random.randint(0, 3000)
      plt.imshow(images[x], cmap='gray', interpolation='nearest')
      dic = {0:'Glioma tumor', 1:'Meningioma tumor', 2:'No tumor', 3:'Pituitary tumor'}
      plt.title(dic.get(labels[x]))
      plt.axis('off')
  plt.savefig('/content/drive/MyDrive/zdjecia.svg', format='svg', dpi=300)
  plt.show()

